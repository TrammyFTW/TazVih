    private void TxtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtSearchKeyReleased
        String s = TxtSearch.getText();
        if(SQLite.openDB()){
            String[][] data = SQLite.searchstocks("product", s);
            String[] column = {"ID", "ProdName","Price","Quantity"};
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(data, column){
                boolean[] canEdit = new boolean[]{
                    false, false, true, true
                };

                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit[columnIndex];
                }
            };
            this.MainInv.setModel(model);
            SQLite.closeDB();
        }
    }//GEN-LAST:event_TxtSearchKeyReleased

