    public void refresh(){
        if(SQLite.openDB()){
            String[][] data = SQLite.read("product");
            String[] column = {"ProductID", "ProductName","Description","Quantity"};
            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(data, column){
                boolean[] canEdit = new boolean[]{
                    false, false, false,false
                };

                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit[columnIndex];
                }
            };
            this.MainInv.setModel(model);
            SQLite.closeDB();
        }
    }
    
